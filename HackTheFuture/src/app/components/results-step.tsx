import { useState } from 'react';
import { FileText, CheckCircle2, AlertTriangle, HelpCircle, RotateCcw, Sparkles, Mail, FileDown, Copy, Check } from 'lucide-react';
import { motion } from 'motion/react';
import type { FormData, AIResults } from '@/app/App';
import jsPDF from 'jspdf';

interface ResultsStepProps {
  formData: FormData;
  results: AIResults;
  onStartOver: () => void;
}

export function ResultsStep({ formData, results, onStartOver }: ResultsStepProps) {
  const [isExporting, setIsExporting] = useState(false);
  const [copied, setCopied] = useState(false);

  const generatePDF = async () => {
    setIsExporting(true);
    try {
      const pdf = new jsPDF('p', 'mm', 'a4');
      const pageWidth = pdf.internal.pageSize.getWidth();
      const margin = 20;
      const contentWidth = pageWidth - 2 * margin;
      let yPos = 20;

      // Header
      pdf.setFontSize(24);
      pdf.setTextColor(13, 148, 136); // teal-600
      pdf.text('MediClarify', margin, yPos);
      yPos += 10;

      pdf.setFontSize(10);
      pdf.setTextColor(107, 114, 128); // gray-500
      pdf.text(`Document: ${formData.file?.name || 'Unknown'}`, margin, yPos);
      yPos += 5;
      pdf.text(`Generated: ${new Date().toLocaleDateString()}`, margin, yPos);
      yPos += 15;

      // Summary Section
      pdf.setFontSize(16);
      pdf.setTextColor(17, 24, 39); // gray-900
      pdf.text('Plain-English Summary', margin, yPos);
      yPos += 8;

      pdf.setFontSize(11);
      pdf.setTextColor(55, 65, 81); // gray-700
      const summaryLines = pdf.splitTextToSize(results.summary, contentWidth);
      pdf.text(summaryLines, margin, yPos);
      yPos += summaryLines.length * 5 + 10;

      // Check if we need a new page
      if (yPos > 250) {
        pdf.addPage();
        yPos = 20;
      }

      // Daily Care Checklist
      pdf.setFontSize(16);
      pdf.setTextColor(17, 24, 39);
      pdf.text('Daily Care Checklist', margin, yPos);
      yPos += 8;

      pdf.setFontSize(11);
      pdf.setTextColor(55, 65, 81);
      results.checklist.forEach((item: string) => {
        if (yPos > 270) {
          pdf.addPage();
          yPos = 20;
        }
        const itemLines = pdf.splitTextToSize(`• ${item}`, contentWidth);
        pdf.text(itemLines, margin, yPos);
        yPos += itemLines.length * 5 + 3;
      });
      yPos += 7;

      // Warning Signs
      if (yPos > 250) {
        pdf.addPage();
        yPos = 20;
      }

      pdf.setFontSize(16);
      pdf.setTextColor(217, 119, 6); // amber-600
      pdf.text('Warning Signs', margin, yPos);
      yPos += 8;

      pdf.setFontSize(11);
      pdf.setTextColor(55, 65, 81);
      results.warnings.forEach((warning: string) => {
        if (yPos > 270) {
          pdf.addPage();
          yPos = 20;
        }
        const warningLines = pdf.splitTextToSize(`⚠ ${warning}`, contentWidth);
        pdf.text(warningLines, margin, yPos);
        yPos += warningLines.length * 5 + 3;
      });
      yPos += 7;

      // Questions for Doctor
      if (yPos > 250) {
        pdf.addPage();
        yPos = 20;
      }

      pdf.setFontSize(16);
      pdf.setTextColor(17, 24, 39);
      pdf.text('Questions to Ask the Doctor', margin, yPos);
      yPos += 8;

      pdf.setFontSize(11);
      pdf.setTextColor(55, 65, 81);
      results.questions.forEach((question: string, index: number) => {
        if (yPos > 270) {
          pdf.addPage();
          yPos = 20;
        }
        const questionLines = pdf.splitTextToSize(`${index + 1}. ${question}`, contentWidth);
        pdf.text(questionLines, margin, yPos);
        yPos += questionLines.length * 5 + 3;
      });

      // Footer
      pdf.setFontSize(9);
      pdf.setTextColor(156, 163, 175); // gray-400
      pdf.text('Generated by MediClarify - For educational purposes only', margin, 285);

      pdf.save(`MediClarify-Summary-${new Date().toISOString().split('T')[0]}.pdf`);
    } catch (error) {
      console.error('Error generating PDF:', error);
    } finally {
      setIsExporting(false);
    }
  };

  const handleEmailShare = () => {
    const subject = encodeURIComponent(`MediClarify Summary - ${formData.file?.name || 'Medical Document'}`);
    const body = encodeURIComponent(
      `MediClarify Summary\n` +
      `==================\n\n` +
      `SUMMARY:\n${results.summary}\n\n` +
      `DAILY CARE CHECKLIST:\n${results.checklist.map((item: string) => `• ${item}`).join('\n')}\n\n` +
      `WARNING SIGNS:\n${results.warnings.map((w: string) => `⚠ ${w}`).join('\n')}\n\n` +
      `QUESTIONS FOR DOCTOR:\n${results.questions.map((q: string, i: number) => `${i + 1}. ${q}`).join('\n')}\n\n` +
      `---\nGenerated by MediClarify`
    );
    window.open(`mailto:?subject=${subject}&body=${body}`);
  };

  const handleCopyAll = async () => {
    const text =
      `MediClarify Summary\n` +
      `==================\n\n` +
      `SUMMARY:\n${results.summary}\n\n` +
      `DAILY CARE CHECKLIST:\n${results.checklist.map((item: string) => `• ${item}`).join('\n')}\n\n` +
      `WARNING SIGNS:\n${results.warnings.map((w: string) => `⚠ ${w}`).join('\n')}\n\n` +
      `QUESTIONS FOR DOCTOR:\n${results.questions.map((q: string, i: number) => `${i + 1}. ${q}`).join('\n')}`;

    await navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const containerVariants = {
    hidden: { opacity: 0 },
    visible: {
      opacity: 1,
      transition: {
        staggerChildren: 0.1
      }
    }
  };

  const itemVariants = {
    hidden: { opacity: 0, y: 20 },
    visible: { opacity: 1, y: 0 }
  };

  return (
    <motion.div
      className="max-w-5xl mx-auto"
      variants={containerVariants}
      initial="hidden"
      animate="visible"
    >
      {/* Success Header */}
      <motion.div variants={itemVariants}>
        <div className="relative bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-8 overflow-hidden">
          <div className="absolute top-0 right-0 w-64 h-64 bg-gradient-to-br from-emerald-400/20 to-transparent rounded-full blur-3xl pointer-events-none" />
          
          <div className="flex items-start justify-between relative">
            <div className="flex items-start gap-5">
              <motion.div 
                className="relative w-16 h-16 rounded-2xl bg-gradient-to-br from-green-500 to-emerald-600 flex items-center justify-center flex-shrink-0 shadow-xl"
                initial={{ scale: 0, rotate: -180 }}
                animate={{ scale: 1, rotate: 0 }}
                transition={{ type: "spring", duration: 0.8, bounce: 0.5 }}
              >
                <CheckCircle2 className="w-9 h-9 text-white" />
                <motion.div
                  className="absolute inset-0 rounded-2xl bg-white"
                  initial={{ opacity: 0.6 }}
                  animate={{ opacity: 0 }}
                  transition={{ duration: 1 }}
                />
              </motion.div>
              <div>
                <motion.h2 
                  className="text-2xl font-bold text-gray-900 mb-2"
                  initial={{ opacity: 0, x: -20 }}
                  animate={{ opacity: 1, x: 0 }}
                  transition={{ delay: 0.3 }}
                >
                  Summary Generated Successfully! 
                </motion.h2>
                <motion.p 
                  className="text-gray-600 mb-3"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  transition={{ delay: 0.4 }}
                >
                  Document: <span className="font-semibold text-gray-900">{formData.file?.name}</span>
                </motion.p>
                <motion.div 
                  className="flex flex-wrap gap-3"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  transition={{ delay: 0.5 }}
                >
                  <span className="inline-flex items-center gap-1.5 px-3 py-1.5 bg-teal-100 text-teal-700 rounded-lg text-sm font-medium">
                    <Sparkles className="w-3.5 h-3.5" />
                    {formData.caregiverType?.replace('-', ' ').split(' ').map((word: string) =>
                      word.charAt(0).toUpperCase() + word.slice(1)
                    ).join(' ')}
                  </span>
                  <span className="inline-flex items-center gap-1.5 px-3 py-1.5 bg-cyan-100 text-cyan-700 rounded-lg text-sm font-medium">
                    <FileText className="w-3.5 h-3.5" />
                    {formData.explanationLevel?.charAt(0).toUpperCase() + formData.explanationLevel?.slice(1)} Mode
                  </span>
                </motion.div>
              </div>
            </div>
            <div className="flex gap-2">
              <motion.button
                onClick={handleCopyAll}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title={copied ? "Copied!" : "Copy all"}
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                {copied ? <Check className="w-5 h-5 text-emerald-600" /> : <Copy className="w-5 h-5 text-gray-600" />}
              </motion.button>
              <motion.button
                onClick={handleEmailShare}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Email summary"
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                <Mail className="w-5 h-5 text-gray-600" />
              </motion.button>
              <motion.button
                onClick={generatePDF}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Download PDF"
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                {isExporting ? (
                  <div className="w-5 h-5 border-2 border-gray-300 border-t-teal-500 rounded-full animate-spin" />
                ) : (
                  <FileDown className="w-5 h-5 text-gray-600" />
                )}
              </motion.button>
              <motion.button
                onClick={onStartOver}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Start over"
                whileHover={{ scale: 1.1, rotate: -180 }}
                whileTap={{ scale: 0.9 }}
                transition={{ duration: 0.3 }}
              >
                <RotateCcw className="w-5 h-5 text-gray-600" />
              </motion.button>
            </div>
          </div>
        </div>
      </motion.div>

      {/* Summary Section */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 right-0 w-48 h-48 bg-gradient-to-br from-teal-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-teal-500 to-cyan-500 flex items-center justify-center shadow-lg shadow-teal-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <FileText className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Plain-English Summary</h3>
          </div>
          <motion.div 
            className="prose prose-gray max-w-none"
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            transition={{ delay: 0.2 }}
          >
            <p className="text-gray-700 leading-relaxed whitespace-pre-line text-base">{results.summary}</p>
          </motion.div>
        </div>
      </motion.div>

      {/* Daily Care Checklist */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 left-0 w-48 h-48 bg-gradient-to-br from-emerald-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-emerald-500 to-green-500 flex items-center justify-center shadow-lg shadow-emerald-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <CheckCircle2 className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Daily Care Checklist</h3>
          </div>
          <div className="space-y-3 relative">
            {results.checklist.map((item: string, index: number) => (
              <motion.div
                key={index}
                className="group flex gap-4 p-4 rounded-xl hover:bg-emerald-50 transition-colors cursor-pointer"
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ x: 4 }}
              >
                <motion.div
                  className="w-6 h-6 rounded-lg border-2 border-gray-300 group-hover:border-emerald-500 flex-shrink-0 mt-0.5 transition-colors"
                  whileHover={{ scale: 1.1 }}
                />
                <span className="text-gray-700 leading-relaxed">{item}</span>
              </motion.div>
            ))}
          </div>
        </div>
      </motion.div>

      {/* Warning Signs */}
      <motion.div variants={itemVariants}>
        <div className="bg-gradient-to-br from-amber-50 to-orange-50 backdrop-blur-xl rounded-3xl shadow-xl border-2 border-amber-200 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 right-0 w-48 h-48 bg-gradient-to-br from-amber-400/20 to-transparent rounded-full blur-3xl pointer-events-none" />
          
          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div 
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-amber-500 to-orange-600 flex items-center justify-center shadow-lg"
              animate={{ 
                rotate: [0, -5, 5, -5, 0],
              }}
              transition={{ 
                duration: 2,
                repeat: Infinity,
                repeatDelay: 3
              }}
            >
              <AlertTriangle className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">What to Watch Out For</h3>
          </div>
          <div className="space-y-3 relative">
            {results.warnings.map((warning: string, index: number) => (
              <motion.div
                key={index}
                className="flex gap-4 p-5 bg-white/80 backdrop-blur-sm rounded-xl border-2 border-amber-200 shadow-sm"
                initial={{ opacity: 0, scale: 0.95 }}
                animate={{ opacity: 1, scale: 1 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ scale: 1.02, borderColor: 'rgb(251 146 60)' }}
              >
                <AlertTriangle className="w-5 h-5 text-amber-600 flex-shrink-0 mt-0.5" />
                <span className="text-gray-800 leading-relaxed font-medium">{warning}</span>
              </motion.div>
            ))}
          </div>
          <motion.div 
            className="mt-6 p-5 bg-red-100 border-2 border-red-300 rounded-2xl"
            initial={{ opacity: 0, y: 10 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.5 }}
          >
            <p className="text-sm text-red-900 leading-relaxed">
              <strong className="font-bold text-base">⚠️ Emergency:</strong> If you notice any of these warning signs, contact your healthcare provider immediately or call 911.
            </p>
          </motion.div>
        </div>
      </motion.div>

      {/* Questions for Doctor */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-8 relative overflow-hidden">
          <div className="absolute top-0 left-0 w-48 h-48 bg-gradient-to-br from-sky-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-sky-500 to-blue-500 flex items-center justify-center shadow-lg shadow-sky-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <HelpCircle className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Questions to Ask the Doctor</h3>
          </div>
          <div className="space-y-3 relative">
            {results.questions.map((question: string, index: number) => (
              <motion.div
                key={index}
                className="group flex gap-4 p-4 rounded-xl hover:bg-sky-50 transition-colors"
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ x: 4 }}
              >
                <motion.div
                  className="w-8 h-8 rounded-xl bg-gradient-to-br from-sky-500 to-blue-500 text-white flex items-center justify-center flex-shrink-0 font-bold shadow-md"
                  whileHover={{ scale: 1.1, rotate: 5 }}
                >
                  {index + 1}
                </motion.div>
                <span className="text-gray-700 leading-relaxed pt-0.5">{question}</span>
              </motion.div>
            ))}
          </div>
        </div>
      </motion.div>

      {/* Action Buttons */}
      <motion.div
        className="flex gap-4"
        variants={itemVariants}
      >
        <motion.button
          onClick={onStartOver}
          className="flex-1 px-6 py-4 border-2 border-gray-300 hover:border-teal-400 hover:bg-white/80 text-gray-700 dark:text-white rounded-2xl font-semibold transition-all backdrop-blur-sm"
          whileHover={{ scale: 1.02 }}
          whileTap={{ scale: 0.98 }}
        >
          Process Another
        </motion.button>
        <motion.button
          onClick={generatePDF}
          disabled={isExporting}
          className="flex-1 px-6 py-4 bg-gradient-to-r from-teal-600 to-cyan-600 hover:from-teal-700 hover:to-cyan-700 text-white rounded-2xl font-semibold shadow-xl shadow-teal-500/30 transition-all flex items-center justify-center gap-2 disabled:opacity-70"
          whileHover={{ scale: 1.02 }}
          whileTap={{ scale: 0.98 }}
        >
          <FileDown className="w-5 h-5" />
          {isExporting ? 'Exporting...' : 'Download PDF'}
        </motion.button>
      </motion.div>
    </motion.div>
  );
}
