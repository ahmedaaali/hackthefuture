import { useState } from 'react';
import { FileText, CheckCircle2, AlertTriangle, HelpCircle, RotateCcw, Sparkles, Mail, FileDown, Copy, Check } from 'lucide-react';
import { motion } from 'motion/react';
import type { FormData, AIResults } from '@/app/App';
import jsPDF from 'jspdf';

interface ResultsStepProps {
  formData: FormData;
  results: AIResults;
  onStartOver: () => void;
}

export function ResultsStep({ formData, results, onStartOver }: ResultsStepProps) {
  const [isExporting, setIsExporting] = useState(false);
  const [copied, setCopied] = useState(false);

  const generatePDF = async () => {
    setIsExporting(true);
    try {
      const pdf = new jsPDF('p', 'mm', 'a4');
      const pageWidth = pdf.internal.pageSize.getWidth();
      const pageHeight = pdf.internal.pageSize.getHeight();
      const margin = 20;
      const contentWidth = pageWidth - 2 * margin;
      let yPos = 0;
      let currentPage = 1;

      // Helper function to add page footer
      const addFooter = (pageNum: number) => {
        // Footer line
        pdf.setDrawColor(229, 231, 235);
        pdf.setLineWidth(0.5);
        pdf.line(margin, pageHeight - 20, pageWidth - margin, pageHeight - 20);

        // Footer text
        pdf.setFontSize(8);
        pdf.setTextColor(156, 163, 175);
        pdf.text('Generated by MediClarify', margin, pageHeight - 12);
        pdf.text('For educational purposes only. Always consult your healthcare provider.', margin, pageHeight - 8);
        pdf.text(`Page ${pageNum}`, pageWidth - margin - 15, pageHeight - 12);
      };

      // Helper function to check and add new page if needed
      const checkNewPage = (requiredSpace: number = 40) => {
        if (yPos > pageHeight - requiredSpace) {
          addFooter(currentPage);
          pdf.addPage();
          currentPage++;
          yPos = 25;
          return true;
        }
        return false;
      };

      // Helper function to draw section header with icon
      const drawSectionHeader = (title: string, color: { r: number; g: number; b: number }, iconType: 'doc' | 'check' | 'warning' | 'question') => {
        checkNewPage(50);

        // Section background bar
        pdf.setFillColor(color.r, color.g, color.b);
        pdf.roundedRect(margin, yPos, contentWidth, 12, 2, 2, 'F');

        // Icon circle
        pdf.setFillColor(255, 255, 255);
        pdf.circle(margin + 8, yPos + 6, 4, 'F');

        // Draw simple icon shape
        pdf.setFillColor(color.r, color.g, color.b);
        if (iconType === 'doc') {
          pdf.rect(margin + 6.5, yPos + 4, 3, 4, 'F');
        } else if (iconType === 'check') {
          pdf.setLineWidth(0.8);
          pdf.setDrawColor(color.r, color.g, color.b);
          pdf.line(margin + 6, yPos + 6, margin + 7.5, yPos + 7.5);
          pdf.line(margin + 7.5, yPos + 7.5, margin + 10, yPos + 4.5);
        } else if (iconType === 'warning') {
          // Triangle shape
          pdf.triangle(margin + 8, yPos + 4, margin + 6, yPos + 8, margin + 10, yPos + 8, 'F');
        } else if (iconType === 'question') {
          pdf.setFontSize(8);
          pdf.setTextColor(color.r, color.g, color.b);
          pdf.text('?', margin + 6.5, yPos + 7.5);
        }

        // Section title
        pdf.setFontSize(12);
        pdf.setTextColor(255, 255, 255);
        pdf.text(title, margin + 16, yPos + 8);

        yPos += 18;
      };

      // ========== HEADER ==========
      // Header background
      pdf.setFillColor(15, 23, 42); // slate-900
      pdf.rect(0, 0, pageWidth, 50, 'F');

      // Gradient accent line
      pdf.setFillColor(13, 148, 136); // teal-600
      pdf.rect(0, 50, pageWidth, 3, 'F');

      // Logo icon (medical cross in circle)
      pdf.setFillColor(13, 148, 136);
      pdf.circle(margin + 12, 25, 10, 'F');
      pdf.setFillColor(255, 255, 255);
      pdf.rect(margin + 9, 21, 6, 2, 'F'); // horizontal bar
      pdf.rect(margin + 11, 19, 2, 6, 'F'); // vertical bar (forms +)
      // Inner circle detail
      pdf.setDrawColor(6, 182, 212);
      pdf.setLineWidth(1);
      pdf.circle(margin + 12, 25, 7, 'S');

      // Title
      pdf.setFontSize(24);
      pdf.setTextColor(255, 255, 255);
      pdf.text('MediClarify', margin + 28, 22);

      // Subtitle
      pdf.setFontSize(10);
      pdf.setTextColor(148, 163, 184); // slate-400
      pdf.text('Your Personal Care Guide', margin + 28, 30);

      // Document info on the right
      pdf.setFontSize(9);
      pdf.setTextColor(148, 163, 184);
      const dateStr = new Date().toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
      });
      pdf.text(dateStr, pageWidth - margin, 18, { align: 'right' });

      // Caregiver type badge
      const caregiverLabel = formData.caregiverType?.replace('-', ' ').split(' ').map((word: string) =>
        word.charAt(0).toUpperCase() + word.slice(1)
      ).join(' ') || 'General';
      pdf.setFillColor(20, 184, 166); // teal-500
      const badgeWidth = pdf.getTextWidth(caregiverLabel) + 8;
      pdf.roundedRect(pageWidth - margin - badgeWidth, 22, badgeWidth, 7, 1.5, 1.5, 'F');
      pdf.setFontSize(8);
      pdf.setTextColor(255, 255, 255);
      pdf.text(caregiverLabel, pageWidth - margin - badgeWidth + 4, 27);

      // Document name
      pdf.setFontSize(8);
      pdf.setTextColor(100, 116, 139);
      const fileName = formData.file?.name || 'Unknown Document';
      const truncatedName = fileName.length > 40 ? fileName.substring(0, 37) + '...' : fileName;
      pdf.text(`Source: ${truncatedName}`, pageWidth - margin, 36, { align: 'right' });

      yPos = 62;

      // ========== SUMMARY SECTION ==========
      drawSectionHeader('Plain-English Summary', { r: 13, g: 148, b: 136 }, 'doc');

      // Summary content box
      pdf.setFillColor(240, 253, 250); // teal-50
      pdf.setDrawColor(153, 246, 228); // teal-200
      pdf.setLineWidth(0.3);
      const summaryLines = pdf.splitTextToSize(results.summary, contentWidth - 12);
      const summaryHeight = summaryLines.length * 5 + 10;
      pdf.roundedRect(margin, yPos, contentWidth, summaryHeight, 3, 3, 'FD');

      pdf.setFontSize(10);
      pdf.setTextColor(55, 65, 81);
      pdf.text(summaryLines, margin + 6, yPos + 8);
      yPos += summaryHeight + 12;

      // ========== DAILY CARE CHECKLIST ==========
      drawSectionHeader('Daily Care Checklist', { r: 16, g: 185, b: 129 }, 'check');

      results.checklist.forEach((item: string, index: number) => {
        checkNewPage(20);

        // Checklist item box
        pdf.setFillColor(index % 2 === 0 ? 255 : 249, index % 2 === 0 ? 255 : 250, index % 2 === 0 ? 255 : 251);
        const itemLines = pdf.splitTextToSize(item, contentWidth - 20);
        const itemHeight = itemLines.length * 5 + 6;
        pdf.roundedRect(margin, yPos, contentWidth, itemHeight, 2, 2, 'F');

        // Checkbox
        pdf.setDrawColor(16, 185, 129);
        pdf.setLineWidth(0.5);
        pdf.roundedRect(margin + 5, yPos + 3, 5, 5, 1, 1, 'S');

        // Item text
        pdf.setFontSize(10);
        pdf.setTextColor(55, 65, 81);
        pdf.text(itemLines, margin + 15, yPos + 7);

        yPos += itemHeight + 2;
      });
      yPos += 8;

      // ========== WARNING SIGNS ==========
      checkNewPage(60);
      drawSectionHeader('Warning Signs - Seek Help If You Notice', { r: 245, g: 158, b: 11 }, 'warning');

      // Warning box background
      pdf.setFillColor(255, 251, 235); // amber-50
      pdf.setDrawColor(252, 211, 77); // amber-300
      pdf.setLineWidth(0.5);

      let warningStartY = yPos;
      let tempY = yPos + 6;

      results.warnings.forEach((warning: string) => {
        const warnLines = pdf.splitTextToSize(warning, contentWidth - 20);
        tempY += warnLines.length * 5 + 8;
      });

      const warningBoxHeight = tempY - warningStartY + 6;
      pdf.roundedRect(margin, yPos, contentWidth, warningBoxHeight, 3, 3, 'FD');
      yPos += 6;

      results.warnings.forEach((warning: string) => {
        checkNewPage(20);

        // Warning icon
        pdf.setFillColor(245, 158, 11);
        pdf.triangle(margin + 8, yPos + 1, margin + 5, yPos + 6, margin + 11, yPos + 6, 'F');
        pdf.setFillColor(255, 255, 255);
        pdf.rect(margin + 7.5, yPos + 2.5, 1, 2, 'F');
        pdf.circle(margin + 8, yPos + 5.2, 0.5, 'F');

        // Warning text
        pdf.setFontSize(10);
        pdf.setTextColor(120, 53, 15); // amber-900
        const warnLines = pdf.splitTextToSize(warning, contentWidth - 20);
        pdf.text(warnLines, margin + 16, yPos + 4);

        yPos += warnLines.length * 5 + 8;
      });
      yPos += 12;

      // Emergency notice
      checkNewPage(30);
      pdf.setFillColor(254, 226, 226); // red-100
      pdf.setDrawColor(248, 113, 113); // red-400
      pdf.roundedRect(margin, yPos, contentWidth, 16, 3, 3, 'FD');

      pdf.setFontSize(10);
      pdf.setTextColor(153, 27, 27); // red-800
      pdf.text('EMERGENCY:', margin + 6, yPos + 7);
      pdf.setFontSize(9);
      pdf.setTextColor(127, 29, 29);
      pdf.text('If you notice any warning signs, contact your healthcare provider immediately or call 911.', margin + 32, yPos + 7);
      yPos += 24;

      // ========== QUESTIONS FOR DOCTOR ==========
      checkNewPage(60);
      drawSectionHeader('Questions to Ask Your Doctor', { r: 14, g: 165, b: 233 }, 'question');

      results.questions.forEach((question: string, index: number) => {
        checkNewPage(20);

        // Question row
        pdf.setFillColor(index % 2 === 0 ? 240 : 248, index % 2 === 0 ? 249 : 250, index % 2 === 0 ? 255 : 252);
        const qLines = pdf.splitTextToSize(question, contentWidth - 20);
        const qHeight = qLines.length * 5 + 8;
        pdf.roundedRect(margin, yPos, contentWidth, qHeight, 2, 2, 'F');

        // Number badge
        pdf.setFillColor(14, 165, 233);
        pdf.circle(margin + 8, yPos + qHeight / 2, 5, 'F');
        pdf.setFontSize(9);
        pdf.setTextColor(255, 255, 255);
        pdf.text(`${index + 1}`, margin + 6.5, yPos + qHeight / 2 + 1.5);

        // Question text
        pdf.setFontSize(10);
        pdf.setTextColor(55, 65, 81);
        pdf.text(qLines, margin + 18, yPos + 6);

        yPos += qHeight + 2;
      });

      // Add final footer
      addFooter(currentPage);

      pdf.save(`MediClarify-Summary-${new Date().toISOString().split('T')[0]}.pdf`);
    } catch (error) {
      console.error('Error generating PDF:', error);
    } finally {
      setIsExporting(false);
    }
  };

  const handleEmailShare = () => {
    const subject = encodeURIComponent(`MediClarify Summary - ${formData.file?.name || 'Medical Document'}`);
    const body = encodeURIComponent(
      `MediClarify Summary\n` +
      `==================\n\n` +
      `SUMMARY:\n${results.summary}\n\n` +
      `DAILY CARE CHECKLIST:\n${results.checklist.map((item: string) => `• ${item}`).join('\n')}\n\n` +
      `WARNING SIGNS:\n${results.warnings.map((w: string) => `⚠ ${w}`).join('\n')}\n\n` +
      `QUESTIONS FOR DOCTOR:\n${results.questions.map((q: string, i: number) => `${i + 1}. ${q}`).join('\n')}\n\n` +
      `---\nGenerated by MediClarify`
    );
    window.open(`mailto:?subject=${subject}&body=${body}`);
  };

  const handleCopyAll = async () => {
    const text =
      `MediClarify Summary\n` +
      `==================\n\n` +
      `SUMMARY:\n${results.summary}\n\n` +
      `DAILY CARE CHECKLIST:\n${results.checklist.map((item: string) => `• ${item}`).join('\n')}\n\n` +
      `WARNING SIGNS:\n${results.warnings.map((w: string) => `⚠ ${w}`).join('\n')}\n\n` +
      `QUESTIONS FOR DOCTOR:\n${results.questions.map((q: string, i: number) => `${i + 1}. ${q}`).join('\n')}`;

    await navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const containerVariants = {
    hidden: { opacity: 0 },
    visible: {
      opacity: 1,
      transition: {
        staggerChildren: 0.1
      }
    }
  };

  const itemVariants = {
    hidden: { opacity: 0, y: 20 },
    visible: { opacity: 1, y: 0 }
  };

  return (
    <motion.div
      className="max-w-5xl mx-auto"
      variants={containerVariants}
      initial="hidden"
      animate="visible"
    >
      {/* Success Header */}
      <motion.div variants={itemVariants}>
        <div className="relative bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-8 overflow-hidden">
          <div className="absolute top-0 right-0 w-64 h-64 bg-gradient-to-br from-emerald-400/20 to-transparent rounded-full blur-3xl pointer-events-none" />
          
          <div className="flex items-start justify-between relative">
            <div className="flex items-start gap-5">
              <motion.div 
                className="relative w-16 h-16 rounded-2xl bg-gradient-to-br from-green-500 to-emerald-600 flex items-center justify-center flex-shrink-0 shadow-xl"
                initial={{ scale: 0, rotate: -180 }}
                animate={{ scale: 1, rotate: 0 }}
                transition={{ type: "spring", duration: 0.8, bounce: 0.5 }}
              >
                <CheckCircle2 className="w-9 h-9 text-white" />
                <motion.div
                  className="absolute inset-0 rounded-2xl bg-white"
                  initial={{ opacity: 0.6 }}
                  animate={{ opacity: 0 }}
                  transition={{ duration: 1 }}
                />
              </motion.div>
              <div>
                <motion.h2 
                  className="text-2xl font-bold text-gray-900 mb-2"
                  initial={{ opacity: 0, x: -20 }}
                  animate={{ opacity: 1, x: 0 }}
                  transition={{ delay: 0.3 }}
                >
                  Summary Generated Successfully! 
                </motion.h2>
                <motion.p 
                  className="text-gray-600 mb-3"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  transition={{ delay: 0.4 }}
                >
                  Document: <span className="font-semibold text-gray-900">{formData.file?.name}</span>
                </motion.p>
                <motion.div 
                  className="flex flex-wrap gap-3"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  transition={{ delay: 0.5 }}
                >
                  <span className="inline-flex items-center gap-1.5 px-3 py-1.5 bg-teal-100 text-teal-700 rounded-lg text-sm font-medium">
                    <Sparkles className="w-3.5 h-3.5" />
                    {formData.caregiverType?.replace('-', ' ').split(' ').map((word: string) =>
                      word.charAt(0).toUpperCase() + word.slice(1)
                    ).join(' ')}
                  </span>
                  <span className="inline-flex items-center gap-1.5 px-3 py-1.5 bg-cyan-100 text-cyan-700 rounded-lg text-sm font-medium">
                    <FileText className="w-3.5 h-3.5" />
                    {formData.explanationLevel?.charAt(0).toUpperCase() + formData.explanationLevel?.slice(1)} Mode
                  </span>
                </motion.div>
              </div>
            </div>
            <div className="flex gap-2">
              <motion.button
                onClick={handleCopyAll}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title={copied ? "Copied!" : "Copy all"}
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                {copied ? <Check className="w-5 h-5 text-emerald-600" /> : <Copy className="w-5 h-5 text-gray-600" />}
              </motion.button>
              <motion.button
                onClick={handleEmailShare}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Email summary"
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                <Mail className="w-5 h-5 text-gray-600" />
              </motion.button>
              <motion.button
                onClick={generatePDF}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Download PDF"
                whileHover={{ scale: 1.1 }}
                whileTap={{ scale: 0.9 }}
              >
                {isExporting ? (
                  <div className="w-5 h-5 border-2 border-gray-300 border-t-teal-500 rounded-full animate-spin" />
                ) : (
                  <FileDown className="w-5 h-5 text-gray-600" />
                )}
              </motion.button>
              <motion.button
                onClick={onStartOver}
                className="p-3 hover:bg-gray-100 rounded-xl transition-colors"
                title="Start over"
                whileHover={{ scale: 1.1, rotate: -180 }}
                whileTap={{ scale: 0.9 }}
                transition={{ duration: 0.3 }}
              >
                <RotateCcw className="w-5 h-5 text-gray-600" />
              </motion.button>
            </div>
          </div>
        </div>
      </motion.div>

      {/* Summary Section */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 right-0 w-48 h-48 bg-gradient-to-br from-teal-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-teal-500 to-cyan-500 flex items-center justify-center shadow-lg shadow-teal-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <FileText className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Plain-English Summary</h3>
          </div>
          <motion.div 
            className="prose prose-gray max-w-none"
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            transition={{ delay: 0.2 }}
          >
            <p className="text-gray-700 leading-relaxed whitespace-pre-line text-base">{results.summary}</p>
          </motion.div>
        </div>
      </motion.div>

      {/* Daily Care Checklist */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 left-0 w-48 h-48 bg-gradient-to-br from-emerald-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-emerald-500 to-green-500 flex items-center justify-center shadow-lg shadow-emerald-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <CheckCircle2 className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Daily Care Checklist</h3>
          </div>
          <div className="space-y-3 relative">
            {results.checklist.map((item: string, index: number) => (
              <motion.div
                key={index}
                className="group flex gap-4 p-4 rounded-xl hover:bg-emerald-50 transition-colors cursor-pointer"
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ x: 4 }}
              >
                <motion.div
                  className="w-6 h-6 rounded-lg border-2 border-gray-300 group-hover:border-emerald-500 flex-shrink-0 mt-0.5 transition-colors"
                  whileHover={{ scale: 1.1 }}
                />
                <span className="text-gray-700 leading-relaxed">{item}</span>
              </motion.div>
            ))}
          </div>
        </div>
      </motion.div>

      {/* Warning Signs */}
      <motion.div variants={itemVariants}>
        <div className="bg-gradient-to-br from-amber-50 to-orange-50 backdrop-blur-xl rounded-3xl shadow-xl border-2 border-amber-200 p-8 mb-6 relative overflow-hidden">
          <div className="absolute top-0 right-0 w-48 h-48 bg-gradient-to-br from-amber-400/20 to-transparent rounded-full blur-3xl pointer-events-none" />
          
          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div 
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-amber-500 to-orange-600 flex items-center justify-center shadow-lg"
              animate={{ 
                rotate: [0, -5, 5, -5, 0],
              }}
              transition={{ 
                duration: 2,
                repeat: Infinity,
                repeatDelay: 3
              }}
            >
              <AlertTriangle className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">What to Watch Out For</h3>
          </div>
          <div className="space-y-3 relative">
            {results.warnings.map((warning: string, index: number) => (
              <motion.div
                key={index}
                className="flex gap-4 p-5 bg-white/80 backdrop-blur-sm rounded-xl border-2 border-amber-200 shadow-sm"
                initial={{ opacity: 0, scale: 0.95 }}
                animate={{ opacity: 1, scale: 1 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ scale: 1.02, borderColor: 'rgb(251 146 60)' }}
              >
                <AlertTriangle className="w-5 h-5 text-amber-600 flex-shrink-0 mt-0.5" />
                <span className="text-gray-800 leading-relaxed font-medium">{warning}</span>
              </motion.div>
            ))}
          </div>
          <motion.div 
            className="mt-6 p-5 bg-red-100 border-2 border-red-300 rounded-2xl"
            initial={{ opacity: 0, y: 10 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.5 }}
          >
            <p className="text-sm text-red-900 leading-relaxed">
              <strong className="font-bold text-base">⚠️ Emergency:</strong> If you notice any of these warning signs, contact your healthcare provider immediately or call 911.
            </p>
          </motion.div>
        </div>
      </motion.div>

      {/* Questions for Doctor */}
      <motion.div variants={itemVariants}>
        <div className="bg-white/80 backdrop-blur-xl rounded-3xl shadow-xl border border-teal-100/50 p-8 mb-8 relative overflow-hidden">
          <div className="absolute top-0 left-0 w-48 h-48 bg-gradient-to-br from-sky-400/10 to-transparent rounded-full blur-3xl pointer-events-none" />

          <div className="flex items-center gap-3 mb-6 relative">
            <motion.div
              className="w-12 h-12 rounded-xl bg-gradient-to-br from-sky-500 to-blue-500 flex items-center justify-center shadow-lg shadow-sky-500/25"
              whileHover={{ rotate: [0, -10, 10, -10, 0] }}
              transition={{ duration: 0.5 }}
            >
              <HelpCircle className="w-6 h-6 text-white" />
            </motion.div>
            <h3 className="text-xl font-bold text-gray-900">Questions to Ask the Doctor</h3>
          </div>
          <div className="space-y-3 relative">
            {results.questions.map((question: string, index: number) => (
              <motion.div
                key={index}
                className="group flex gap-4 p-4 rounded-xl hover:bg-sky-50 transition-colors"
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: 0.1 + index * 0.05 }}
                whileHover={{ x: 4 }}
              >
                <motion.div
                  className="w-8 h-8 rounded-xl bg-gradient-to-br from-sky-500 to-blue-500 text-white flex items-center justify-center flex-shrink-0 font-bold shadow-md"
                  whileHover={{ scale: 1.1, rotate: 5 }}
                >
                  {index + 1}
                </motion.div>
                <span className="text-gray-700 leading-relaxed pt-0.5">{question}</span>
              </motion.div>
            ))}
          </div>
        </div>
      </motion.div>

      {/* Action Buttons */}
      <motion.div
        className="flex gap-4"
        variants={itemVariants}
      >
        <motion.button
          onClick={onStartOver}
          className="flex-1 px-6 py-4 border-2 border-gray-300 hover:border-teal-400 hover:bg-white/80 text-gray-700 dark:text-white rounded-2xl font-semibold transition-all backdrop-blur-sm"
          whileHover={{ scale: 1.02 }}
          whileTap={{ scale: 0.98 }}
        >
          Process Another
        </motion.button>
        <motion.button
          onClick={generatePDF}
          disabled={isExporting}
          className="flex-1 px-6 py-4 bg-gradient-to-r from-teal-600 to-cyan-600 hover:from-teal-700 hover:to-cyan-700 text-white rounded-2xl font-semibold shadow-xl shadow-teal-500/30 transition-all flex items-center justify-center gap-2 disabled:opacity-70"
          whileHover={{ scale: 1.02 }}
          whileTap={{ scale: 0.98 }}
        >
          <FileDown className="w-5 h-5" />
          {isExporting ? 'Exporting...' : 'Download PDF'}
        </motion.button>
      </motion.div>
    </motion.div>
  );
}
